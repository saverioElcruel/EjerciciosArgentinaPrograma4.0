/*
El método charAt en Java se utiliza para obtener un carácter 
de una cadena en una posición específica. 
*/
package ejemplos.string;

public class charAt {

    public static void main(String[] args) {
        String cadena = "Camino al Queso";
        char decima = cadena.charAt(10);
        System.out.println(decima); 
    }
    
}
